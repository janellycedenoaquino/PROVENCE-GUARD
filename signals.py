import os
import re
import json
import math
from groq import Groq
from dotenv import load_dotenv

load_dotenv()

_client = Groq(api_key=os.environ.get("GROQ_API_KEY"))

PROMPT_TEMPLATE = """You are an expert at distinguishing between human-written and AI-generated text.

Analyze the text below and assess whether it was written by a human or generated by AI.

Consider:
- Sentence structure variety and unpredictability
- Vocabulary diversity and naturalness
- Presence of personal voice, emotion, or idiosyncrasies
- Statistical uniformity (AI text tends to be more consistent and polished)

Respond ONLY with a JSON object in this exact format:
{{"ai_probability": <float between 0.0 and 1.0>, "reasoning": "<one sentence explanation>"}}

Where ai_probability is:
- Close to 1.0 if the text strongly appears AI-generated
- Close to 0.0 if the text strongly appears human-written
- Around 0.5 if you are uncertain

Text to analyze:
\"\"\"
{text}
\"\"\"

JSON only. No other text."""


IMAGE_PROMPT_TEMPLATE = """You are an expert at distinguishing between human-written and AI-generated image descriptions.

Analyze the image description below and assess whether it was written by a human or generated by AI.

AI-generated image descriptions tend to be:
- Clinically precise and formulaic ("A woman sits at a minimalist desk, bathed in warm afternoon light")
- Impersonal, lacking subjective perspective or emotional reaction
- Structured as Subject + action/position + setting + lighting/color details
- Free of casual language, personal context, or conversational tone

Human image descriptions tend to be:
- Casual and context-aware ("my friend took this at sunset, you can barely see us")
- Subjective and emotionally inflected
- Incomplete or focused on what mattered to the person, not a comprehensive scene inventory
- Varied in structure — not always a full sentence

Respond ONLY with a JSON object in this exact format:
{{"ai_probability": <float between 0.0 and 1.0>, "reasoning": "<one sentence explanation>"}}

Where ai_probability is:
- Close to 1.0 if the description strongly appears AI-generated
- Close to 0.0 if the description strongly appears human-written
- Around 0.5 if you are uncertain

Image description to analyze:
\"\"\"
{text}
\"\"\"

JSON only. No other text."""


def classify_with_llm(text: str, content_type: str = "text") -> dict:
    template = IMAGE_PROMPT_TEMPLATE if content_type == "image_description" else PROMPT_TEMPLATE
    response = _client.chat.completions.create(
        model="llama-3.3-70b-versatile",
        messages=[{"role": "user", "content": template.format(text=text)}],
        temperature=0.1,
        max_tokens=200,
    )

    raw = response.choices[0].message.content.strip()

    try:
        result = json.loads(raw)
        return {
            "ai_probability": float(result["ai_probability"]),
            "reasoning": result.get("reasoning", ""),
        }
    except (json.JSONDecodeError, KeyError, ValueError):
        return {"ai_probability": 0.5, "reasoning": "Could not parse model response."}


def compute_stylometric_score(text: str) -> float:
    """
    Returns a 0-1 score where higher = more AI-like.
    Combines two metrics:
      - Sentence length variance (low variance = AI-like)
      - Type-token ratio (low vocabulary diversity = AI-like)
    """
    words = re.findall(r"\b\w+\b", text.lower())
    sentences = [s.strip() for s in re.split(r"[.!?]+", text) if s.strip()]

    if not words or not sentences:
        return 0.5

    # Metric 1: sentence length variance
    # AI text has uniform sentence lengths → low std dev → high AI score
    lengths = [len(re.findall(r"\b\w+\b", s)) for s in sentences]
    if len(lengths) > 1:
        mean = sum(lengths) / len(lengths)
        std_dev = math.sqrt(sum((l - mean) ** 2 for l in lengths) / len(lengths))
    else:
        std_dev = 0.0
    # std_dev of 0 → score 1.0 (AI-like); std_dev >= 10 → score 0.0 (human-like)
    variance_score = max(0.0, 1.0 - (std_dev / 10.0))

    # Metric 2: type-token ratio (vocabulary diversity)
    # High TTR → human; Low TTR → AI
    ttr = len(set(words)) / len(words)
    ttr_score = 1.0 - ttr  # invert: low diversity = high AI score

    style_score = (0.5 * variance_score) + (0.5 * ttr_score)
    return round(min(1.0, max(0.0, style_score)), 4)


def compute_word_length_score(text: str) -> float:
    """
    Returns a 0-1 score where higher = more AI-like.
    Measures average word length — AI text favors longer, more sophisticated vocabulary.
    Normalized: avg 3 chars → 0.0 (casual/human), avg 7+ chars → 1.0 (formal/AI).
    """
    words = re.findall(r"\b\w+\b", text.lower())
    if not words:
        return 0.5
    avg_len = sum(len(w) for w in words) / len(words)
    score = (avg_len - 3) / (7 - 3)
    return round(min(1.0, max(0.0, score)), 4)


def compute_confidence(llm_score: float, style_score: float, word_len_score: float) -> dict:
    """
    Ensemble: combines 3 signals into a single confidence score.
    Weights: LLM 50%, stylometric 30%, word length 20%.
    Thresholds: >= 0.70 → likely_ai, 0.35–0.70 → uncertain, < 0.35 → likely_human
    """
    combined = round(
        (0.50 * llm_score) + (0.30 * style_score) + (0.20 * word_len_score),
        4
    )

    if combined >= 0.70:
        attribution = "likely_ai"
    elif combined >= 0.35:
        attribution = "uncertain"
    else:
        attribution = "likely_human"

    return {"score": combined, "attribution": attribution}


